def anchor(text: str) -> str:
    t = text.strip()
    t = re.sub(r"[（(].*?[）)]", "", t)
    t = t.lower()
    t = re.sub(r"[^\w\u4e00-\u9fff\s-]", "", t)
    t = re.sub(r"\s+", "-", t)
    return t


def build_toc(content: str) -> str:
    lines = ["## 目录", "", "> 点击章节标题可快速跳转。", ""]
    for m in re.finditer(r"^(#{2,4})\s+(.+)$", content, re.M):
        level, title = m.group(1), m.group(2).strip()
        if title == "目录":
            continue
        depth = len(level)
        if depth == 2:
            lines.append(f"- [{title}](#{anchor(title)})")
        elif depth == 3 and re.match(r"^\d+\.", title):
            lines.append(f"  - [{title}](#{anchor(title)})")
        elif depth == 4 and re.match(r"^\d+\.\d+", title):
            lines.append(f"    - [{title}](#{anchor(title)})")
    lines.extend(["", "---", ""])
    return "\n".join(lines)


def replace_toc(text: str) -> str:
    start = text.find("## 目录")
    if start == -1:
        return text
    end = text.find("\n---\n", start)
    if end == -1:
        return text
    body_without_toc = text[:start] + text[end + len("\n---\n") :]
    toc = build_toc(body_without_toc)
    hr = text.find("\n---\n", 0)
    return text[: hr + len("\n---\n")] + toc + body_without_toc[body_without_toc.find("## 1.") :]


