def anchor(text: str) -> str:
    t = text.strip()
    t = re.sub(r"[（(].*?[）)]", "", t)
    t = t.lower()
    t = re.sub(r"[^\w\u4e00-\u9fff\s-]", "", t)
    t = re.sub(r"\s+", "-", t)
    return t


def build_toc(content: str) -> str:
    lines = []
    lines.append("## 目录")
    lines.append("")
    lines.append("> 点击章节标题可快速跳转。")
    lines.append("")
    for m in re.finditer(r"^(#{2,3})\s+(.+)$", content, re.M):
        level, title = m.group(1), m.group(2).strip()
        if title == "目录":
            continue
        if level == "##":
            lines.append(f"- [{title}](#{anchor(title)})")
        elif level == "###" and re.match(r"^\d+\.", title):
            indent = "  " if re.match(r"^\d+\.\d+", title) else ""
            lines.append(f"{indent}- [{title}](#{anchor(title)})")
    lines.append("")
    lines.append("---")
    lines.append("")
    return "\n".join(lines)


